package Section4.Demos.Switch;

public class Main {
	public static void main(String[] args) {
		int month = 3;
		switch (month) {
		case 1:
			System.out.println("Jan");
			break;
		case 2:
			System.out.println("FEB");
			break;
		case 3:
			System.out.println("MAR");
			break;
		case 4:
			System.out.println("APR");
			break;
		default:
			System.out.println("IDK");
		}
		
	}

}
